# COMPARISON OPERATORS
# OPERATOR    NAME                       EXAMPLE
#  ==         equal                      a==b
#  !=        Not equal                   a!=b
# >         Greater than                 a>b
# <         Less than                    a<b
# >=      Greater than or Equal TO       a>=b
# <=      Less than or Equal TO          a<=b


x =10
y=12
print(x==y)

print("-----------------------------------------------")

x =10
y=12
print(x==y)

print("-----------------------------------------------")
x =10
y=12
print(x<y)

print("-----------------------------------------------")
x =10
y=12
print(x!=y)

print("-----------------------------------------------")

